
#include "cmap-root.h"

#include "cmap-common.h"
#include "cmap-kernel.h"

/*******************************************************************************
*******************************************************************************/

static const char * root__nature(CMAP_ROOT * this)
{
  return "cmap.nature.root";
}

/*******************************************************************************
*******************************************************************************/

static void root__delete(CMAP_ROOT * this)
{
  cmap_root_delete(this);
}

/*******************************************************************************
*******************************************************************************/

void cmap_root_init(CMAP_ROOT * root)
{
  root -> nature = root__nature;
  root -> delete = root__delete;
}

/*******************************************************************************
*******************************************************************************/

void cmap_root_delete(CMAP_ROOT * root)
{
  CMAP_FREE(root);
}
